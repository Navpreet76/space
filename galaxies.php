<h4>Spiral galaxy M100</h4>

<p>Spiral galaxies have three main components: a bulge, disk, and halo (see right). The bulge is a spherical structure found in the center of the galaxy. This feature mostly contains older stars. The disk is made up of dust, gas, and younger stars. The disk forms arm structures. Our Sun is located in an arm of our galaxy, the Milky Way. The halo of a galaxy is a loose, spherical structure located around the bulge and some of the disk. The halo contains old clusters of stars, known as globular clusters.</p>





