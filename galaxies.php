<h1>Galaxies</h1>
<p>Spiral galaxies are classified into two groups, ordinary and barred. The ordinary group is designated by S or SA, and the barred group by SB. In normal spirals (as seen at above left) the arms originate directly from the nucleus, or bulge, where in the barred spirals (see right) there is a bar of material that runs through the nucleus that the arms emerge from. Both of these types are given a classification according to how tightly their arms are wound. The classifications are a, b, c, d ... with "a" having the tightest arms. In type "a", the arms are usually not well defined and form almost a circular pattern. Sometimes you will see the classification of a galaxy with two lower case letters. This means that the tightness of the spiral structure is halfway between those two letters.</p>
