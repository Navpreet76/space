<h4>Irregular Galaxies</h4>

<p>Irregular galaxies have no regular or symmetrical structure. They are divided into two groups, Irr I and IrrII. Irr I type galaxies have HII regions, which are regions of elemental hydrogen gas, and many Population I stars, which are young hot stars. Irr II galaxies simply seem to have large amounts of dust that block most of the light from the stars. All this dust makes is almost impossible to see distinct stars in the galaxy.</p>
