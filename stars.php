<p>
If a star has between 1.35 and 2.1 times the mass of the Sun, it doesn’t form a white dwarf when it dies. Instead, the star dies in a catastrophic supernova explosion, and the remaining core becomes a neutron star. As its name implies, a neutron star is an exotic type of star that is composed entirely of neutrons. This is because the intense gravity of the neutron star crushes protons and electrons together to form neutrons. If stars are even more massive, they will become black holes instead of neutron stars after the supernova goes off.</p>
