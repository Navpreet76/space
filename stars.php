<h1>Red Giant Star</h1>
<p>When a star has consumed its stock of hydrogen in its core, fusion stops and the star no longer generates an outward pressure to counteract the inward pressure pulling it together. A shell of hydrogen around the core ignites continuing the life of the star, but causes it to increase in size dramatically.</p><p>The aging star has become a red giant star, and can be 100 times larger than it was in its main sequence phase. When this hydrogen fuel is used up, further shells of helium and even heavier elements can be consumed in fusion reactions.</p><p> The red giant phase of a star’s life will only last a few hundred million years before it runs out of fuel completely and becomes a white dwarf.</p>
