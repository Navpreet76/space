<hr>
<div id="demo3" class="collapse">

<h4>The majority of all stars in our galaxy, and even the Universe, are main sequence stars.</h4><p>Our Sun is a main sequence star, and so are our nearest neighbors, Sirius and Alpha Centauri A. Main sequence stars can vary in size, mass and brightness, but they’re all doing the same thing: converting hydrogen into helium in their cores, releasing a tremendous amount of energy.</p>
<p>A star in the main sequence is in a state of hydrostatic equilibrium. Gravity is pulling the star inward, and the light pressure from all the fusion reactions in the star are pushing outward. The inward and outward forces balance one another out, and the star maintains a spherical shape.Stars in the main sequence will have a size that depends on their mass, which defines the amount of gravity pulling them inward.
The lower mass limit for a main sequence star is about 0.08 times the mass of the Sun, or 80 times the mass of Jupiter.This is the minimum amount of gravitational pressure you need to ignite fusion in the core. Stars can theoretically grow to more than 100 times the mass of the Sun.</p>
