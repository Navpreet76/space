<h1>White Dwarf Star</h1>
<p>When a star has completely run out of hydrogen fuel in its core and it lacks the mass to force higher elements into fusion reaction, it becomes a white dwarf star. The outward light pressure from the fusion reaction stops and the star collapses inward under its own gravity.</p><p> A white dwarf shines because it was a hot star once, but there’s no fusion reactions happening any more. A white dwarf will just cool down until it because the background temperature of the Universe. This process will take hundreds of billions of years, so no white dwarfs have actually cooled down that far yet.</p>

