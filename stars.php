<h2>T Tauri Star</h2>
<h4>A T Tauri star is stage in a star’s formation and evolution right before it becomes a main sequence star.</h4><p>This phase occurs at the end of the protostar phase, when the gravitational pressure holding the star together is the source of all its energy.</p><p>T Tauri stars don’t have enough pressure and temperature at their cores to generate nuclear fusion, but they do resemble main sequence stars; they’re about the same temperature but brighter because they’re a larger.</p> <p>T Tauri stars can have large areas of sunspot coverage, and have intense X-ray flares and extremely powerful stellar winds. Stars will remain in the T Tauri stage for about 100 million years.</p>
