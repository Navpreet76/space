          <h1 class="text-info">Saturn’s Rings</h1>
			<p>While all the gas giants in our solar system have rings none of them are as extensive or distinctive as Saturn’s. The rings were discovered by Galileo Galilei 1610 who observed them with a telescope. The first ‘up close’ view of the rings were by Pioneer 11 spacecraft which flew by Saturn on September 1, 1971.</p>
		
			<p>Saturn’s rings are made up of are billions of particles that range in size from tiny dust grains to to objects as large as mountains. These are made up of chunks of ice and rock, believed to have come from asteroids comets or even moons, that broke apart before they reached the planet.</p>
			<p>Saturn’s rings are divided into 7 groups, named alphabetically in the order of their discovery (Outwards from Saturn; D, C, B, A, F, G and E). The F ring is kept in place by two of Saturn’s moons, Prometheus and Pandora, these are referred to as ‘shepherd moons’. Other satellites are responsible for creating divisions in the rings as well as shepherding them.</p>

			<h1 class="text-info">Saturn’s Atmosphere</h1>
			Saturn’s atmosphere is composed primarily of hydrogen (96%) and helium (3%) with traces of other substances like methane, ammonia, acetylene, ethane, propane and phosphine. Winds in the upper atmosphere can reach speeds of 500 metres a second, these combined with heat rising from within the planet’s interior cause yellow and gold bands.</p>


