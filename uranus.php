<h1>Facts About Uranus</h1>
<h4>Uranus was officially discovered by Sir William Herschel in 1781.</h4>
<p>It is too dim to have been seen by the ancients. At first Herschel thought it was a comet, but several years later it was confirmed as a planet. Herscal tried to have his discovery named “Georgian Sidus” after King George III. The name Uranus was suggested by astronomer Johann Bode. The name comes from the ancient Greek deity Ouranos.</p>
<p>Uranus turns on its axis once every 17 hours, 14 minutes.
The planet rotates in a retrograde direction, opposite to the way Earth and most other planets turn.</p>
<h4>Uranus makes one trip around the Sun every 84 Earth years.
During some parts of its orbit one or the other of its poles point directly at the Sun and get about 42 years of direct sunlight. The rest of the time they are in darkness.</h4>
<h4>Uranus is often referred to as an “ice giant” planet.</h4>
<p>Like the other gas giants, it has a hydrogen upper layer, which has helium mixed in. Below that is an icy “mantle, which surrounds a rock and ice core. The upper atmosphere is made of water, ammonia and the methane ice crystals that give the planet its pale blue colour.</p>
</div>
<div class="col-md-4">

<h4>Uranus has two sets of very thin dark coloured rings.</h4>
<p>The ring particles are small, ranging from a dust-sized particles to small boulders. There are eleven inner rings and two outer rings. They probably formed when one or more of Uranus’s moons were broken up in an impact. The first rings were discovered in 1977 with the two outer rings being discovered in Hubble Space Telescope images between 2003 and 2005.</p>
<p>Uranus’ moons are named after characters created by William Shakespeare and Alexander Pope.
These include Oberon, Titania and Miranda.  All are frozen worlds with dark surfaces. Some are ice and rock mixtures.  The most interesting Uranian moon is Miranda; it has ice canyons, terraces, and other strange-looking surface areas.
Only one spacecraft has flown by Uranus.</p>
<p>In 1986, the Voyager 2 spacecraft swept past the planet at a distance of 81,500 km. It returned the first close-up images of the planet, its moons, and rings.</p>
