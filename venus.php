<div class="col-md-4">
<p>A day on Venus lasts longer than a year. 
It takes 243 Earth days to rotate once on its axis (sidereal day). The planet’s orbit around the Sun takes 225 Earth days, compared to the Earth’s 365. A day on the surface of Venus (solar day) takes 117 Earth days.
Venus rotates in the opposite direction to most other planets. </p>
<p>This means that Venus is rotating in the opposite direction to the Sun, this is also know as a retrograde rotation. A possible reason might be a collision in the past with an asteroid or other object that caused the planet to alter its rotational path. It also differs from most other planets in our solar system by having no natural satellites.</p>
<h4>Venus is the second brightest object in the night sky. </h4>
<p>Only the Moon is brighter. With a magnitude of between -3.8 to -4.6 Venus is so bright it can be seen during daytime on a clear day.</p>
<p>Atmospheric pressure on Venus is 92 times greater than the Earth’s. 
While its size and mass are similar to Earth, the small asteroids are crushed when entering its atmosphere, meaning no small craters lie on the surface of the planet. The pressure felt by a human on the surface would be equivalent to that experienced deep beneath the sea on Earth.</p>
<h4>Venus is often called the Earth’s sister planet.</h4>
</div>
<div class="col-md-4"> 
<p>The Earth and Venus are very similar in size with only a 638 km difference in diameter, Venus having 81.5% of the Earth’s mass. Both also have a central core, a molten mantle and a crust.
Venus is also known as the Morning Star and the Evening Star.</p> 
<p>Early civilisations thought Venus was two different bodies, called Phosphorus and Hesperus by the Greeks, and Lucifer and Vesper by the Romans. This is because when its orbit around the Sun overtakes Earth’s orbit, it changes from being visible after sunset to being visible before sunrise. Mayan astronomers made detailed observations of Venus as early as 650 AD.</p>
<h4>Venus is the hottest planet in our solar system. </h4>
<p>The average surface temperature is 462 °C, and because Venus does not tilt on its axis, there is no seasonal variation. The dense atmosphere of around 96.5 percent carbon dioxide traps heat and causes a greenhouse effect.
A detailed study of Venus finished in 2015. </p>
<p>In 2006, the Venus Express space craft was sent into orbit around Venus by the European Space Agency, and sent back information about the planet. Originally planned to last five hundred Earth days, the mission was extended several times before the craft was finally deorbited in 2015. More than 1,000 volcanoes or volcanic centres larger than 20 km have been found on the surface of Venus.</p>
