<h1>Jupiter’s Great Red Spot</h1>
<p>Situated 22° south of Jupiter’s equator, the Great Red Spot is a storm that has been raging for at least 186 years. In fact, upper estimates suggest that this red and turbulent storm could have been in existence for over three and a half centuries. A giant red spot was seen on Jupiter in the seventeenth century, when telescopes first started to be used. However, it is unknown whether this is the same red spot that we see today, or whether Jupiter has had many such storms that have come and gone as the centuries pass.</p>

<p>The red spot circulates anticlockwise and takes six (earth) days to rotate completely. Another mystery surrounding the red spot is what makes it red: scientists have come up with several theories (for instance, the presence of red organic compounds) but as yet nobody knows for certain. That will be a question for future astronomy!</p>
