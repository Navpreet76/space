<h1>Facts About Jupiter</h1>
<h4>Jupiter is the fourth brightest object in the solar system.</h4>
<p>Only the Sun, Moon and Venus are brighter. It is one of five planets visible to the naked eye from Earth.
The ancient Babylonians were the first to record their sightings of Jupiter.
This was around the 7th or 8th century BC. Jupiter is named after the king of the Roman gods. To the Greeks, it represented Zeus, the god of thunder. The Mesopotamians saw Jupiter as the god Marduk and patron of the city of Babylon. Germanic tribes saw this planet as Donar, or Thor.</p>
<h4>Jupiter has the shortest day of all the planets.</h4>
<p>It turns on its axis once every 9 hours and 55 minutes. The rapid rotation flattens the planet slightly, giving it an oblate shape.</p>
<h4>Jupiter orbits the Sun once every 11.8 Earth years.</h4>
<p>From our point of view on Earth, it appears to move slowly in the sky, taking months to move from one constellation to another.</p>
</div>
<div class="col-md-4">
<h4>Jupiter has unique cloud features.</h4>
<p>The upper atmosphere of Jupiter is divided into cloud belts and zones. They are made primarily of ammonia crystals, sulfur, and mixtures of the two compounds.</p>
<h4>The Great Red Spot is a huge storm on Jupiter.
It has raged for at least 350 years. It is so large that three Earths could fit inside it.</h4>
<p>Jupiter’s interior is made of rock, metal, and hydrogen compounds.</p>
Below Jupiter’s massive atmosphere (which is made primarily of hydrogen), there are layers of compressed hydrogen gas, liquid metallic hydrogen, and a core of ice, rock, and metals.
Jupiter’s moon Ganymede is the largest moon in the solar system.
Jupiter’s moons are sometimes called the Jovian satellites, the largest of these are Ganymeade, Callisto Io and Europa. Ganymeade measures 5,268 km across, making it larger than the planet Mercury.
Jupiter has a thin ring system.
Its rings are composed mainly of dust particles ejected from some of Jupiter’s smaller worlds during impacts from incoming comets and asteroids. The ring system begins some 92,000 kilometres above Jupiter’s cloud tops and stretches out to more than 225,000 km from the planet. They are between 2,000 to 12,500 kilometres thick.
Eight spacecraft have visited Jupiter.
Pioneer 10 and 11, Voyager 1 and 2, Galileo, Cassini, Ulysses, and New Horizons missions. The Juno mission is its way to Jupiter and will arrive in July 2016. Other future missions may focus on the Jovian moons Europa, Ganymede, and Callisto, and their subsurface oceans.
