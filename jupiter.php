<h1>Jupiter’s Atmosphere</h1>
<p>Jupiter’s atmosphere is special because it is the solar system’s largest planetary atmosphere. It is made up of hydrogen and helium, in roughly the same proportions as are found in the sun. However, it also contains much smaller amounts of other space gases, such as ammonia, methane and water. 90% of the atmosphere of Jupiter – a huge proportion – is made of hydrogen. It would be impossible for humans to breathe in this atmosphere. So, if you are thinking of travelling to space to do some hands on astronomy research, you would have to wear a breathing suit when visiting this planet.</p>
